//メソッドの呼び出し
public class practice51 {
  //メインメソッド
  public static void main(String[] args) {
    System.out.println("メソッドを呼び出します");
    //helloメソッドを呼び出す
    hello();
    System.out.println("メソッドの呼び出しが終わりました");
  }
  //helloメソッドの本体
  public static void hello() {
    System.out.println("渚さん、こんにちは");
  }
}